package com.ericsson.oss.itpf.security.kaps.model;

import java.io.Serializable;
import java.security.KeyPair;

/**
 * Data model that identifies {@link KeyPair}.
 * 
 */
public class KeyIdentifier implements Serializable {

    private static final long serialVersionUID = 8140247595873578933L;

    private String id;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id
     *            the id to set
     */
    public void setId(final String id) {
        this.id = id;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        return result;
    }

    @Override
    public boolean equals(final Object obj) {
        if (this == obj) {
            return true;
        }

        if (obj == null) {
            return false;
        }

        if (getClass() != obj.getClass()) {
            return false;
        }

        final KeyIdentifier other = (KeyIdentifier) obj;

        if (id == null) {
            if (other.id != null) {
                return false;
            }
        } else if (!id.equals(other.id)) {
            return false;
        }

        return true;
    }

    @Override
    public String toString() {
        return "KeyIdentifier [id=" + id + "]";
    }

}